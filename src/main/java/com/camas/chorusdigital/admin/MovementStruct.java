package com.camas.chorusdigital.admin;

public class MovementStruct {

	String title;
	String notes;

	public MovementStruct() {}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getNotes() {
		return notes;
	}

	public void setNotes(String notes) {
		this.notes = notes;
	}
}
